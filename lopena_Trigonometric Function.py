import math

# Example of sine function
angle_degrees = 30
angle_radians = math.radians(angle_degrees)
result_sin = math.sin(angle_radians)
print("Sine of", angle_degrees, "degrees is", result_sin)

# Example of cosine function
result_cos = math.cos(angle_radians)
print("Cosine of", angle_degrees, "degrees is", result_cos)