import math

# Example of power function
base = 2
exponent = 3
result_power = math.pow(base, exponent)
print(base, "raised to the power", exponent, "is", result_power)

# Example of logarithmic function
num_log = 100
result_log = math.log(num_log, 10)
print("Logarithm of", num_log, "to base 10 is", result_log)