import math

# Example of degrees to radians conversion
angle_degrees = 45
angle_radians = math.radians(angle_degrees)
print(angle_degrees, "degrees in radians is", angle_radians)

# Example of radians to degrees conversion
angle_radians = math.pi / 4
angle_degrees = math.degrees(angle_radians)
print(angle_radians, "radians in degrees is", angle_degrees)